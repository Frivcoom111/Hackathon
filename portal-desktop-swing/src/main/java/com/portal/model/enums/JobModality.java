package com.portal.model.enums;

/**
 * Enum JobModality (Modalidade da Vaga): define o formato de trabalho de uma vaga (Job).
 */
public enum JobModality {
    PRESENCIAL, // Presencial: o trabalho é feito no local da empresa.
    REMOTE,     // Remoto: o trabalho é feito totalmente à distância (home office).
    HYBRID      // Híbrido: mistura dias presenciais e dias remotos.
}
